package com.cosmosource.common.model;

public class LevelModel {

	private String levelId;
	private String levelCode;
	private String changeLevelCode;
	private String levelName;
	private String company;
	private String remark;
	private String orgIds;
	
	public String getLevelId() {
		return levelId;
	}
	public void setLevelId(String levelId) {
		this.levelId = levelId;
	}
	public String getLevelCode() {
		return levelCode;
	}
	public void setLevelCode(String levelCode) {
		this.levelCode = levelCode;
	}
	public String getLevelName() {
		return levelName;
	}
	public void setLevelName(String levelName) {
		this.levelName = levelName;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getOrgIds() {
		return orgIds;
	}
	public void setOrgIds(String orgIds) {
		this.orgIds = orgIds;
	}
	public String getChangeLevelCode() {
		return changeLevelCode;
	}
	public void setChangeLevelCode(String changeLevelCode) {
		this.changeLevelCode = changeLevelCode;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
